def by_lead_time(lt):
    return [c for c in CASES if c.get("lead_time") == lt]

def by_slug(slug):
    for c in CASES:
        if c["slug"] == slug:
            return c
    return None

def by_course(course_code):
    return [c for c in CASES if course_code in c.get("courses", [])]

def by_tag(tag):
    return [c for c in CASES if tag in c.get("tags", [])]

def search(query):
    q = query.lower()
    return [c for c in CASES if
            q in c["title"].lower() or
            q in c.get("chief_complaint", "").lower() or
            q in c.get("diagnosis", "").lower() or
            q in " ".join(c.get("aliases", [])).lower() or
            q in " ".join(c.get("tags", [])).lower() or
            q in c.get("course", "").lower()]

